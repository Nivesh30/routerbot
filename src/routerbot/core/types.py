# ---------------------------------------------------------------------------
# Audio — Transcription Request / Response
# ---------------------------------------------------------------------------


class AudioTranscriptionRequest(BaseModel):
    """OpenAI-compatible ``/audio/transcriptions`` request body.

    Note: the actual ``file`` field is handled as form data at the proxy
    layer; this model covers the non-file parameters only.
    """

    model: str = "whisper-1"
    language: str | None = None
    prompt: str | None = None
    response_format: str | None = None  # json, text, srt, verbose_json, vtt
    temperature: float | None = None
    timestamp_granularities: list[str] | None = None

    model_config = {"extra": "allow"}


class AudioTranscriptionResponse(BaseModel):
    """OpenAI-compatible transcription response (JSON mode)."""

    text: str

    model_config = {"extra": "allow"}


# ---------------------------------------------------------------------------
# Rerank — Request / Response (Cohere-style, growing ecosystem support)
# ---------------------------------------------------------------------------


class RerankRequest(BaseModel):
    """Rerank request (Cohere/Jina compatible)."""

    model: str
    query: str
    documents: list[str] | list[dict[str, Any]]
    top_n: int | None = None
    return_documents: bool | None = None

    model_config = {"extra": "allow"}


class RerankResult(BaseModel):
    """A single reranked document."""

    index: int
    relevance_score: float
    document: dict[str, Any] | None = None


class RerankResponse(BaseModel):
    """Rerank response."""

    id: str = Field(default_factory=lambda: f"rerank-{uuid.uuid4().hex[:24]}")
    results: list[RerankResult] = Field(default_factory=list)
    model: str = ""
    usage: EmbeddingUsage | None = None

    model_config = {"extra": "allow"}


# ---------------------------------------------------------------------------
# Model Info — metadata registry entry
# ---------------------------------------------------------------------------


class ModelCard(BaseModel):
    """Model metadata as returned by ``/models`` and used in the registry.

    This is the runtime model info (distinct from the config-level
    ``ModelInfo`` used in ``routerbot_config.yaml``).
    """

    id: str
    object: Literal["model"] = "model"
    created: int = Field(default_factory=lambda: int(time.time()))
    owned_by: str = "routerbot"
    max_input_tokens: int | None = None
    max_output_tokens: int | None = None
    input_cost_per_token: float | None = None
    output_cost_per_token: float | None = None
    supports_vision: bool = False
    supports_function_calling: bool = False
    supports_streaming: bool = True
    context_window: int | None = None

    model_config = {"extra": "allow"}


class ModelListResponse(BaseModel):
    """OpenAI-compatible ``/models`` list response."""

    object: Literal["list"] = "list"
    data: list[ModelCard] = Field(default_factory=list)
